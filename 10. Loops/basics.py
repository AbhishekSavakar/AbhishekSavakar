# range(stop)
# range(start,stop)
# range(start,Stop,step)   stop number is excluded.
# range number are printed using loops
for i in range(20):
    print(i, end=' ')
print()
for i in range(0, 20):
    print(i, end=' ')
print()
for i in range(0, 20, 1):
    print(i, end=' ')
print()
for i in range(19, -1, -1):
    print(i, end=' ')
print()
name = 'Abhishek'
# To reverse string
print(name[::-1])
